import { createServerClient, type CookieOptions } from '@supabase/ssr'
import { NextResponse, type NextRequest } from 'next/server'

export async function middleware(request: NextRequest) {
  let response = NextResponse.next({
    request: {
      headers: request.headers,
    },
  })

  // For demo purposes, use dummy values if environment variables are not set
  const supabaseUrl = process.env.NEXT_PUBLIC_SUPABASE_URL || 'https://demo.supabase.co'
  const supabaseAnonKey = process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY || 'demo-key'

  const supabase = createServerClient(
    supabaseUrl,
    supabaseAnonKey,
    {
      cookies: {
        get(name: string) {
          return request.cookies.get(name)?.value
        },
        set(name: string, value: string, options: CookieOptions) {
          request.cookies.set({
            name,
            value,
            ...options,
          })
          response = NextResponse.next({
            request: {
              headers: request.headers,
            },
          })
          response.cookies.set({
            name,
            value,
            ...options,
          })
        },
        remove(name: string, options: CookieOptions) {
          request.cookies.set({
            name,
            value: '',
            ...options,
          })
          response = NextResponse.next({
            request: {
              headers: request.headers,
            },
          })
          response.cookies.set({
            name,
            value: '',
            ...options,
          })
        },
      },
    }
  )

  const { data: { user } } = await supabase.auth.getUser()

  // Protected routes
  const protectedPaths = ['/dashboard']
  const isProtectedPath = protectedPaths.some(path => 
    request.nextUrl.pathname.startsWith(path)
  )

  if (isProtectedPath && !user) {
    return NextResponse.redirect(new URL('/login', request.url))
  }

  // Role-based redirects
  if (user) {
    const { data: profile } = await supabase
      .from('profiles')
      .select('role')
      .eq('id', user.id)
      .single()

    if (profile) {
      // Admin routes
      if (request.nextUrl.pathname.startsWith('/admin') && profile.role !== 'admin') {
        return NextResponse.redirect(new URL('/', request.url))
      }

      // Teacher routes  
      if (request.nextUrl.pathname.startsWith('/dashboard/teacher') && profile.role !== 'teacher') {
        return NextResponse.redirect(new URL('/dashboard/student/dashboard', request.url))
      }

      // Student routes
      if (request.nextUrl.pathname.startsWith('/dashboard/student') && profile.role !== 'student') {
        return NextResponse.redirect(new URL('/dashboard/teacher/dashboard', request.url))
      }

      // Redirect from generic /dashboard to role-specific dashboard
      if (request.nextUrl.pathname === '/dashboard') {
        const roleRedirect = profile.role === 'student' 
          ? '/dashboard/student/dashboard'
          : profile.role === 'teacher'
          ? '/dashboard/teacher/dashboard'
          : '/admin/dashboard'
        return NextResponse.redirect(new URL(roleRedirect, request.url))
      }
    }
  }

  return response
}

export const config = {
  matcher: [
    /*
     * Match all request paths except for the ones starting with:
     * - _next/static (static files)
     * - _next/image (image optimization files)
     * - favicon.ico (favicon file)
     * - public (public files)
     */
    '/((?!_next/static|_next/image|favicon.ico|public).*)',
  ],
}